public class Coach {
    private String name;
    private String surname;
    private int age;
    private String type;
    private String email;
    private String phoneNumber;

    public Coach(String name, String surname, int age, String type, String email, String phoneNumber) {
        this.name = name;
        this.surname = surname;
        this.age = age;
        this.type = type;
        this.email = email;
        this.phoneNumber = phoneNumber;
    }
}